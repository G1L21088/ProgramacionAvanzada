
public class Valor
{
	private String simb;
	private String valor;
	
	public Valor(String simb, String valor)
	{
		this.simb = simb;
		this.valor = valor;
	}
	
	public String getSimb()
	{
		return simb;
	}
	
	public void setSimb(String simb)
	{
		this.simb = simb;
	}
	
	public String getValor()
	{
		return valor;
	}
	
	public void setValor(String valor)
	{
		this.valor = valor;
	}
}
