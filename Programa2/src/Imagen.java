
public class Imagen
{
	private String name;
	private int cols;
	private int reng;
	
	public Imagen(String name, int cols, int reng)
	{
		this.name = name;
		this.cols = cols;
		this.reng = reng;
	}
	
	public String getName()
	{
		return name;
	}
	
	public int getCols()
	{
		return cols;
	}
	
	public int getReng()
	{
		return reng;
	}
	
}
